SELECT 
YEAR(order_date) AS YEAR,
MONTH(order_date)AS MONTH,
SUM(AMOUNT)AS monthly_revenue,
COUNT(DISTINCT product_id)AS monthly_order_value
FROM
online_sales
GROUP BY YEAR(order_date),
MONTH(order_date)
ORDER BY monthly_revenue DESC
LIMIT 20;

